<p className="my-3 leading-relaxed">Even though this feature seems like a nice gesture from your bank, you could easily get yourself in financial trouble. If you have a low balance in your checking account and need to make a few small purchases, every one of them could rack up a $25 penalty. And you won’t a receive a warning this is happening. If your checking account is regularly low at the end of each month, this penalty can be devastating. You can opt out of overdraft at any bank, which means your debit card will be declined if you try to make a purchase without sufficient funds in your account. This could cause a temporary embarrassment while shopping, but your finances will thank you later.</p>

