<p className="my-3 leading-relaxed">On the other hand, a spending plan is much simpler. Start by noting down your fixed expenses that stay the same each month, then add in your other essential costs. What you’re left with is your <strong>discretionary income</strong>, which you can use for whatever you like. So, if you want to treat yourself to burritos with friends, go for it! Just remember, that you’ll have less to spend on other discretionary items as a result. You won’t have to start from scratch if you go over budget.</p>

